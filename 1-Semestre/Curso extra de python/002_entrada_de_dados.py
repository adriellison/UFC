x = int(input("Digite o primeiro número: "))
y = int(input("Digite o segundo número: "))

print("\nO resultado da soma é: ", x + y)
print("O resultado da subtração é: ", x - y)
print("O resultado da multiplicação é: ", x * y)
print("O resultado da divisão real é: ", x / y)
print("O resultado da divisão inteira é: ", x // y)
print("O resultado da potência é: ", x ** y)